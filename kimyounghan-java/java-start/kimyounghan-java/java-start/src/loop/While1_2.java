package loop;

public class While1_2 {
    public static void main(String[] args) {
        int count = 1;
        while (count <= 3) {
            ++count;
            System.out.println("현재 숫자는: " + count);
//while문은 게임할 때 계속 돌아간다.
//게임이 계속 뭔가 돌아간다. 종료 누를때까지 계속 돌아간다.(내상각엔 레벨업도 while문인 것 같다.)
        }
    }
}
